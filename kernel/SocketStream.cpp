#include "lisp.hpp"
#include "SocketStream.hpp"

bool SocketStream::typep(Value type) const
{
  return (type == S_socket_stream || type == S_stream || type == S_atom || type == T
          || type == C_socket_stream || type == C_stream || type == C_t);
}

